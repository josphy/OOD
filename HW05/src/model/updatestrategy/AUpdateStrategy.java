package model.updatestrategy;

import model.Ball;
import model.IUpdateStrategy;

/**
 * An abstract implementation of IUpdateStrategy.
 *
 * @param <TDispMsg> The type of message sent to the registered IObservers
 */
public abstract class AUpdateStrategy<TDispMsg> implements IUpdateStrategy<TDispMsg> {
  /**
   * Initialize the strategy. This method should be called every time the Ball
   * sets a new strategy.
   *
   * Implementation: Do nothing.
   *
   * @param context  The ball using this strategy.
   */
  @Override
  public void init(Ball context) {}

}
