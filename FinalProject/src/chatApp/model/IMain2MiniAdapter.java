package chatApp.model;

import javax.swing.JPanel;

import chatRoom.model.ChatRoomModel;

/**
 * main model to mini mvc adapter
 */
public interface IMain2MiniAdapter {

	/**
	 * get mini model
	 * @return mini model
	 */
	public ChatRoomModel getMiniModel();

	/**
	 * get mini view panel
	 * @return mini panel to add into new tab
	 */
	public JPanel getMiniView();

	/**
	 * quit current room
	 */
	public void quitRoom();

}
