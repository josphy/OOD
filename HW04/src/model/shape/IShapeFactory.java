package model.shape;

import java.awt.Shape;

/**
 * Interface of shape factory. Implement it so that a class could make shape objects.
 */
public interface IShapeFactory {

	/**
	 * Call this method to make a shape object
	 * @param x The x location of the shape
	 * @param y The y location of the shape
	 * @param xScale The x-dimension of the shape
	 * @param yScale The y-dimension of the shape
	 * @return The shape object to make
	 */
	public Shape makeShape(double x, double y, double xScale, double yScale);
}
